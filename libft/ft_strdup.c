#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dup;
	int		size;
	int		i;

	size = 0;
	while (src[size])
		size++;
	dup = (char *)malloc(sizeof(char) * (size + 1));
	if (!dup)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
